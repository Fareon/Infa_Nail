import turtle
import math

turtle.shape('turtle')
turtle.penup()
turtle.forward(-325)
turtle.pendown()

d0 = [(50, 0, 0), (100, -90, 0), (50, -90, 0), (100, -90, 0), (50, -90, 0)]
d1 = [(0, -90, 50), (math.sqrt(2)*50, 135, 0), (100, -135, 0), (100, 180, 0), (0, -90, 0)]
d2 = [(50, 0, 0), (50, -90, 0), (math.sqrt(2)*50, -45, 0), (50, 135, 0), (0, 90, 100), (0, -90, 0)]
d3 = [(50, 0, 0), (math.sqrt(2)*50, -135, 0), (50, 135, 0), (math.sqrt(2)*50, -135, 0), (0, 135, 50), (0, 90, 100), (0, -90, 0)]
d4 = [(50, -90, 0), (50, 90, 0), (50, 90, 0), (100, 180, 0),(100, 180, 0), (0, -90, 0)]
d5 = [(50, -90, 0), (50, 90, 0), (50, -90, 0), (50, -90, 0), (0, -90, 100), (50, -90, 0)]
d6 = [(0, -90, 50), (50, 90, 0), (50, -90, 0), (50, -90, 0), (50, -90, 0), (math.sqrt(2)*50, -45, 0), (0, -45, 0)]
d7 = [(50, 0, 0), (math.sqrt(2)*50, -135, 0), (50, 45, 0), (0, 90, 50), (0, 90, 100), (0, -90, 0)]
d8 = [(50, 0, 0), (50, -90, 0), (50, -90, 0), (50, 90, 0), (50, 90, 0), (50, 90, 0), (50, 90, 0), (50, -90, 0), (50, -90, 0)]
d9 = [(0, -90, 100), (math.sqrt(2)*50, 135, 0), (50, 45, 0), (50, 90, 0), (50, 90, 0), (50, 90, 0), (50, 90, 0), (0, -90, 0)]

d = [d0, d1, d2, d3, d4, d5, d6, d7, d8, d9]
a = str(input())

for i in range(len(a)):
    g = int(a[i])
    turtle.penup()
    turtle.forward(50)
    turtle.pendown()
    for lenght, angle, inv in d[g]:
        turtle.left(angle)
        turtle.forward(lenght)
        turtle.penup()
        turtle.forward(inv)
        turtle.pendown()





